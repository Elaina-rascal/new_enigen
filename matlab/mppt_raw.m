function Te = mppt_raw(Wg)
    % 600kW 风机最优转矩法 (OTC) —— 带防失速软起步保护
    % 输入: Wg - 发电机实际转速 (rad/s)
    % 输出: Te - 发电机电磁转矩指令 (N·m)
    
    % 1. 定义核心参数
    W_gbgn = 54;  % 发电机界限转速 (对应切入风速)
    K_opt = 0.0626;  % 最优转矩系数
    
    % 2. 分段控制逻辑 (防气动死锁)
    if Wg < W_gbgn
        % 起步阶段：转速过低时，强制松开电磁刹车，让气动力全力推风轮加速
        Te = 0; 
    else
        % 追踪阶段：转速达标后，严格按照最优转矩曲线施加力矩发电
        Te = K_opt * (Wg^2); 
    end
end
